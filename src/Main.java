import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
//		For loop executes a block of code a limited amount of times
		for(int i = 0; i <= 10; i++) {
			System.out.println("Count up: "+ i);
		}
		
		for(int i = 10; i >= 0; i--) {
			System.out.println("Count down: "+ i);
		}
	}
}
